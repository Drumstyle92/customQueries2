package it.develhope.customQueries2.entities;

/**
 * @author Drumstyle92
 * Enumerated with constant variables to determine various flight statuses
 */
public enum FlightStatus {

    /**
     * Ontime Constant variable that represents the on time status of the flight
     */
    ONTIME,

    /**
     * Delayed Constant variable representing the flight delay status
     */
    DELAYED,

    /**
     * Cancelled Constant variable representing the flight cancellation status
     */
    CANCELLED

}